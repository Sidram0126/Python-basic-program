# 11 Set: Create two sets and perform union, intersection, and difference.
set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}
# Union
union_set = set1 | set2
print("Union:", union_set)
# Intersection
intersection_set = set1 & set2
print("Intersection:", intersection_set)
# Difference
difference_set = set1 - set2
print("Difference:", difference_set)
print("------------------------------")